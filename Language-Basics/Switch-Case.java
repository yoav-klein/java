
class SwitchCase {
    static public void main(String[] args) {
            int a = 7;
            switch(a) {
                case 5: System.out.println("A is 5");
                        break;
                case 10: System.out.println("A is 10");
                        break;
                default: System.out.println("A is something else");
                        break;

            }
        }
}