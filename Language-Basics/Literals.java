
class Literals {
    static public void main(String[] args) {

        // integral literals are of type int by default

        int a = 10; // decimal integral literal
        int b = 0xB; // hex-decimal integral literal
        int c = 0b100; // binary integral literal

        long l = 1231432l; // ends with l - of type long 

        // string literals
        String s = "Hello world"; // string literals in double quotes
        char ch = 'c'; // char literals in single quotes
    }
}