

class Primitives {
    public static void main(String[] args) {
        // byte - -128 to 127
        byte b = 12;
        System.out.println(b);

        // short - -32768 to 32767
        short sh = 30000;
        System.out.println(sh);

        // int - -2^31 to 2^31-1
        int i = 34453;
        System.out.println(i);

        // long - -2^63 to 2^63-1
        long l = 17179869184;

        // float and double
        float f = 1.234;
        System.out.println(f);

        double d = 1.234;
        System.out.println(d);

        // boolean
        boolean bool = true;
        System.out.println(bool);

        // char - 16 bit unicode
        

    }
}